//************************ Quetzal Button ********************

$(document).ready(function (){
    $('#quetzal-button').click(function() {
        $("#quetzal-modal").toggleClass("visible");
        $(".modal").toggleClass("visible");
        });
});

$(document).ready(function (){
    $('#quetzal-close').click(function() {
        $("#quetzal-modal").toggleClass("visible")
        $(".modal").toggleClass("visible");
        });
});

